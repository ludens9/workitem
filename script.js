// 점수 계산 함수
function calculateScores(answers) {
    const scores = {
        올바른: 0,
        신비롭고: 0,
        명석한: 0,
        튼튼한: 0,
        빛나는: 0,
        예리한: 0
    };
    
    // Q1-Q7 답변에 대한 점수 계산
    for (let i = 0; i < 7; i++) {
        const questionNum = i + 1;
        const answerText = answers[i];
        
        if (answerText && answerScoring[questionNum] && answerScoring[questionNum][answerText]) {
            const answerScore = answerScoring[questionNum][answerText];
            Object.keys(answerScore).forEach(key => {
                scores[key] += answerScore[key];
            });
        }
    }
    
    return scores;
}

// 최종 결과 결정 함수
function determineResult(answers) {
    const scores = calculateScores(answers);
    
    // A 키워드 (올바른, 신비롭고, 명석한) 중 최고점
    const aKeywords = ['올바른', '신비롭고', '명석한'];
    const aScores = aKeywords.map(key => ({ key, score: scores[key] }));
    const topA = aScores.reduce((max, current) => current.score > max.score ? current : max);
    
    // B 키워드 (튼튼한, 빛나는, 예리한) 중 최고점
    const bKeywords = ['튼튼한', '빛나는', '예리한'];
    const bScores = bKeywords.map(key => ({ key, score: scores[key] }));
    const topB = bScores.reduce((max, current) => current.score > max.score ? current : max);
    
    // Q8 답변에서 아이템 결정
    const q8AnswerText = answers[7]; // 8번째 답변 (0-based index)
    const item = itemMapping[q8AnswerText] || "마법서";
    
    // 결과 키 생성 및 매핑
    const resultKey = `${topA.key}_${topB.key}_${item}`;
    const resultId = resultMapping[resultKey] || 1;
    
    return {
        resultId,
        aKeyword: topA.key,
        bKeyword: topB.key,
        item,
        scores,
        title: `${topA.key}고 ${topB.key}한 ${item}`
    };
}

