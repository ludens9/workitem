// 질문 페이지 업데이트
function updateQuestionPage() {
    const questionData = questions[currentQuestion - 1];
    
    // 질문 제목에서 {name}을 실제 이름으로 치환
    let questionTitle = questionData.title;
    if (userName) {
        questionTitle = questionTitle.replace(/{name}/g, userName);
    }
    
    // 질문 제목 업데이트
    document.querySelector('.question-title').textContent = `Q${currentQuestion}. ${questionTitle}`;
    
    // 답변 버튼 업데이트
    const answerButtons = document.querySelector('.answer-buttons');
    answerButtons.innerHTML = '';
    
    questionData.answers.forEach((answer, index) => {
        const button = document.createElement('button');
        
        // 3,4,5번 질문은 타이틀+부연설명 구조
        if (currentQuestion >= 3 && currentQuestion <= 5 && typeof answer === 'object') {
            button.className = 'answer-button btn_line';
            button.innerHTML = `
                <div class="answer-button-complex">
                    <div class="answer-title h2b">${answer.title}</div>
                    <div class="answer-description h3b">${answer.description}</div>
                </div>
            `;
        } else {
            button.className = 'answer-button btn_line h2b';
            button.textContent = typeof answer === 'string' ? answer : answer.title;
        }
        
        // 답변 텍스트를 직접 전달
        const answerText = typeof answer === 'string' ? answer : answer.title;
        button.onclick = () => selectAnswer(currentQuestion, answerText);
        answerButtons.appendChild(button);
    });
    
    // 진행 상태바 업데이트
    updateProgressBar();
}

