// 로고 애니메이션 함수들
function startLogoAnimation() {
    if (isLogoAnimating) return;
    
    isLogoAnimating = true;
    const heroLogo = document.querySelector('.hero-logo');
    if (!heroLogo) return;
    
    let isFirstImage = true;
    
    logoAnimationInterval = setInterval(() => {
        if (isFirstImage) {
            // logo_h.png로 변경
            heroLogo.src = 'images/logo_h.png';
            heroLogo.classList.add('fade-in');
            heroLogo.classList.remove('fade-out');
        } else {
            // logo_hh.png로 변경
            heroLogo.src = 'images/logo_hh.png';
            heroLogo.classList.add('fade-in');
            heroLogo.classList.remove('fade-out');
        }
        
        isFirstImage = !isFirstImage;
    }, 150); // 150ms마다 이미지 변경 (약 6.7fps)
}

function stopLogoAnimation() {
    if (logoAnimationInterval) {
        clearInterval(logoAnimationInterval);
        logoAnimationInterval = null;
    }
    isLogoAnimating = false;
}

