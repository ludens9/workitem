function shareResult() {
    const title = '내 전설템은 무엇일까?';
    const text = '자고 일어났더니 이세계 탑티어 프리랜서가 되었다! 내 업무능력을 증폭시키는 전설템을 찾아보세요!';
    const url = window.location.origin + window.location.pathname;
    
    if (navigator.share) {
        navigator.share({
            title: title,
            text: text,
            url: url
        }).catch(err => {
            console.log('공유 실패:', err);
            // 공유 실패 시 클립보드 복사로 대체
            fallbackShare(url);
        });
    } else {
        fallbackShare(url);
    }
}

// 공유 대체 기능 (클립보드 복사) - 메인 페이지용
function fallbackShare(url) {
    if (navigator.clipboard && navigator.clipboard.writeText) {
        navigator.clipboard.writeText(url).then(() => {
            alert('링크가 클립보드에 복사되었습니다!');
        }).catch(err => {
            console.log('클립보드 복사 실패:', err);
            // 클립보드 API 실패 시 수동 복사 안내
            promptManualCopy(url);
        });
    } else {
        // 클립보드 API 미지원 시 수동 복사 안내
        promptManualCopy(url);
    }
}

// 수동 복사 안내 - 메인 페이지용
function promptManualCopy(url) {
    const textArea = document.createElement('textarea');
    textArea.value = url;
    document.body.appendChild(textArea);
    textArea.select();
    
    try {
        document.execCommand('copy');
        alert('링크가 복사되었습니다!');
    } catch (err) {
        alert(`링크를 수동으로 복사해주세요: ${url}`);
    }
    
    document.body.removeChild(textArea);
}

// 타이핑 애니메이션 변수
let typingInterval = null;
let isTyping = false;

// 타이핑 애니메이션 함수 (자연스러운 타이핑 효과)
function typeWriter(text, elementId, speed = 50) {
    const element = document.getElementById(elementId);
    if (!element) return;
    
    const cursor = document.getElementById('cursor');
    
    // 이전 타이핑 애니메이션이 진행 중이면 중단
    if (isTyping && typingInterval) {
        clearTimeout(typingInterval);
        isTyping = false;
    }
    
    let i = 0;
    element.textContent = '';
    isTyping = true;
    
    function type() {
        if (i < text.length && isTyping) {
            const currentChar = text.charAt(i);
            element.textContent += currentChar;
            i++;
            
            // 자연스러운 타이핑을 위한 다양한 지연시간
            let delay = speed;
            
            // 공백에서 약간 더 긴 멈춤 (단어 사이 생각하는 시간)
            if (currentChar === ' ') {
                delay = speed * 1.5;
            }
            // 줄바꿈에서 더 긴 멈춤 (문장 끝에서 생각하는 시간)
            else if (currentChar === '\n') {
                delay = speed * 3;
            }
            // 문장 부호에서 약간 멈춤
            else if (currentChar === '.' || currentChar === ',' || currentChar === '!' || currentChar === '?') {
                delay = speed * 2;
            }
            // 일반 문자는 약간의 랜덤 변화로 더 자연스럽게
            else {
                delay = speed + (Math.random() * 20 - 10); // ±10ms 랜덤
            }
            
            typingInterval = setTimeout(type, delay);
        } else {
            // 타이핑 완료 또는 중단됨
            isTyping = false;
            if (i >= text.length && cursor) {
                // 타이핑이 완료된 경우에만 커서 표시
                cursor.style.display = 'inline';
            }
        }
    }
    
    // 커서 숨기기 (타이핑 중에는 커서 표시 안함)
    if (cursor) {
        cursor.style.display = 'none';
    }
    type();
}





